package com.app.campeonatovirtualdefutebol.dao;

public class Time {

    private int id = 0;
    private String nome = "";
    private String liga = "";

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public String getLiga() {
        return liga;
    }

    public void setLiga(String liga) {

        this.liga = liga;
    }
}
